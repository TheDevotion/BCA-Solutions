# Define a vector
my_vector <- c(2, 4, 6, 8, 10)

# Calculate the sum
sum_result <- sum(my_vector)

# Calculate the mean
mean_result <- mean(my_vector)

# Calculate the product
product_result <- prod(my_vector)

# Print the results
cat("Sum:", sum_result, "\n")
cat("Mean:", mean_result, "\n")
cat("Product:", product_result, "\n")
