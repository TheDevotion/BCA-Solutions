# Define a vector
my_vector <- c(10, 5, 20, 15, 30, 25)

# Sort the vector in ascending order
sorted_ascending <- sort(my_vector)

# Sort the vector in descending order
sorted_descending <- sort(my_vector, decreasing = TRUE)

# Print the sorted vectors
cat("Sorted in ascending order:", sorted_ascending, "\n")
cat("Sorted in descending order:", sorted_descending, "\n")
